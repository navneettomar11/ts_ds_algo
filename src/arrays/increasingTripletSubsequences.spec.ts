import {expect} from 'chai';

function increasingTriplet(nums: number[]): boolean {
	if(nums.length < 3) {
		return false;
	}
	let min = Infinity;
	let max = Infinity;
	for(let num of nums) {		
		if(num <= max) max = num;
		else if(num <= min) min = num
		else{ 
			return true;
		}
	}

	return false;

};




